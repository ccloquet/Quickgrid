var lang 	= navigator.language || navigator.userLanguage;
var mylang      = lang.substr(0,2)

function translate(mylang)
{
	if (mylang in msgs)
	{
		msg 	= msgs[mylang]
		title	= titles[mylang]
		html	= htmls[mylang]
	}

	for (var id in title)
	{	
		if( title.hasOwnProperty( id ) )
		{
			$('#' + id).attr('title', title[id])
		}	
	}
	for (var id in html)
	{
		if( html.hasOwnProperty( id ) )
		{
			console.log(id)
			$('#' + id).text(html[id])
		}
	}
}
